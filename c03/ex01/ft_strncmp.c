int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		if (*s1 > *s2)
			return (1);
		if (*s1 < *s2)
			return (-1);
		if (*s1 == 0 && *s2 == 0)
			break ;
		++i;
		++s1;
		++s2;
	}
	return (0);
}
